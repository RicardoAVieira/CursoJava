package Controle;

public class For1 {

	public static void main(String[] args) {
		int contador = 1;

		while (contador <= 10) {

			System.out.printf("Bom dia, valor de Contador = %d \n", contador);
			contador++;
		}

		for (int a = 1; a <= 10; a++) {
			System.out.printf("Bom dia, valor de Contador 2 = %d \n", a);
		}

	}

}
